    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
String bookID = jTextField1.getText();
String studentID = jTextField2.getText();

try {
    Connection con = connectionprovider.getcon();
    Statement st = con.createStatement();
    ResultSet rs = st.executeQuery("SELECT * FROM issue WHERE BookID = '" + bookID + "' AND StudentID = '" + studentID + "'");
    
    if (rs.next()) {
        // Check the return status
        if (rs.getString("ReturnBook").equals("NO")) {
            // Book has been returned
            st.executeUpdate("UPDATE issue SET ReturnBook = 'YES' WHERE BookID = '" + bookID + "' AND StudentID = '" + studentID + "'");
            JOptionPane.showMessageDialog(null, "Book successfully returned");
        } else {
            // Book was already returned
            JOptionPane.showMessageDialog(null, "This book has already been returned.");
        }
        setVisible(false);
        new returnBook().setVisible(true);
    } else {
        JOptionPane.showMessageDialog(null, "Book is not issued or the details are incorrect.");
        setVisible(false);
        new returnBook().setVisible(true);
    }
    con.close();
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Connection error: " + e.getMessage());
}


    }//GEN-LAST:event_jButton1ActionPerformed

