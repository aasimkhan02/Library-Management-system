    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
String BookID = jTextField1.getText();
String studentID = jTextField2.getText();
String issueDate = jTextField3.getText();
String dueDate = jTextField4.getText();
String returnBook = "NO"; // Set return status to NO initially

try {
    Connection con = connectionprovider.getcon();
    Statement st = con.createStatement();
    st.executeUpdate("INSERT INTO issue (BookID, StudentID, IssueDate, DueDate, ReturnBook) " +
                    "VALUES ('" + BookID + "', '" + studentID + "', '" + issueDate + "', '" + dueDate + "', '" + returnBook + "')");
    JOptionPane.showMessageDialog(null, "Book successfully issued");
    setVisible(false);
    new issuebook().setVisible(true);
    con.close();
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Connection error: " + e.getMessage());
}





    }//GEN-LAST:event_jButton1ActionPerformed

